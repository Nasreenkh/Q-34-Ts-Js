// Array of favorite pizza names
var pizzas = ['Pepperoni', 'Margherita', 'BBQ Chicken'];
// Loop through the array to print each pizza name and a corresponding statement
for (var i = 0; i < pizzas.length; i++) {
    console.log("I like ".concat(pizzas[i], " pizza."));
}
// Statement about how much you like pizza
console.log("Pizza is one of my favorite foods, I really love it!");
